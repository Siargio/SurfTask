import UIKit

extension UIFont {

    static func proDisplayMedium16() -> UIFont? {
        return UIFont.init(name: "SF Pro Display Medium", size: 16)
    }

    static func proDisplayMedium14() -> UIFont? {
        return UIFont.init(name: "SF Pro Display Medium", size: 14)
    }

    static func proDisplayRegular14() -> UIFont? {
        return UIFont.init(name: "SF Pro Display Regular", size: 14)
    }

    static func proDisplayBold24() -> UIFont? {
        return UIFont.init(name: "SF Pro Display Bold", size: 24)
    }
}
